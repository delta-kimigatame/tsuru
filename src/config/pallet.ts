export interface Pallet {
  /** ピアノロールの白鍵部分の色 */
  whiteKey: string;
  /** ピアノロールの黒鍵部分の色 */
  blackKey: string;
  /** ピアノロールの音高毎のセパレータの色 */
  horizontalSeparator: string;
  /** ピアノロールの拍子毎のセパレータの色 */
  verticalSeparator: string;
  /** 休符ノートの色 */
  restNote: string;
  /** 休符ノートの色(選択時) */
  selectedRestNote: string;
  /** ノートの色 */
  note: string;
  /** ノートの色(選択時) */
  selectedNote: string;
  /** ノートの枠線の色 */
  noteBorder: string;
  /** 歌詞の文字色 */
  lyric: string;
  /** テンポ・ラベルの文字色 */
  tempo: string;
  /** 注意表示の文字色 */
  attention: string;
  /** ピッチ */
  pitch: string;
  /** ピアノロールの白鍵部分の色 */
  tonemapWhiteKey: string;
  /** ピアノロールの黒鍵部分の色 */
  tonemapBlackKey: string;
  /** 再生中の位置を表すシークバーの色 */
  seekBar: string;
}

interface ColorPallet {
  light: Pallet;
  dark: Pallet;
}

