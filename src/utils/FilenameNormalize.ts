/**
 * utf-8(mac)をutf-8に変換する。
 * 対象となる文字を列挙して変換する方式をとる。
 *  */
export const normalizeJP = (value: string): string => {
  const nfdTxt = [
    /が/g,
    /ぎ/g,
    /ぐ/g,
    /げ/g,
    /ご/g,
    /ざ/g,
    /じ/g,
    /ず/g,
    /ぜ/g,
    /ぞ/g,
    /だ/g,
    /ぢ/g,
    /づ/g,
    /で/g,
    /ど/g,
    /ば/g,
    /び/g,
    /ぶ/g,
    /べ/g,
    /ぼ/g,
    /ガ/g,
    /ギ/g,
    /グ/g,
    /ゲ/g,
    /ゴ/g,
    /ザ/g,
    /ジ/g,
    /ズ/g,
    /ゼ/g,
    /ゾ/g,
    /ダ/g,
    /ヂ/g,
    /ヅ/g,
    /デ/g,
    /ド/g,
    /バ/g,
    /ビ/g,
    /ブ/g,
    /ベ/g,
    /ボ/g,
    /ぱ/g,
    /ぴ/g,
    /ぷ/g,
    /ぺ/g,
    /ぽ/g,
    /パ/g,
    /ピ/g,
    /プ/g,
    /ペ/g,
    /ポ/g,
    /ヴ/g,
  ];
  const nfcTxt = [
    "が",
    "ぎ",
    "ぐ",
    "げ",
    "ご",
    "ざ",
    "じ",
    "ず",
    "ぜ",
    "ぞ",
    "だ",
    "ぢ",
    "づ",
    "で",
    "ど",
    "ば",
    "び",
    "ぶ",
    "べ",
    "ぼ",
    "ガ",
    "ギ",
    "グ",
    "ゲ",
    "ゴ",
    "ザ",
    "ジ",
    "ズ",
    "ゼ",
    "ゾ",
    "ダ",
    "ヂ",
    "ヅ",
    "デ",
    "ド",
    "バ",
    "ビ",
    "ブ",
    "ベ",
    "ボ",
    "ぱ",
    "ぴ",
    "ぷ",
    "ぺ",
    "ぽ",
    "パ",
    "ピ",
    "プ",
    "ペ",
    "ポ",
    "ヴ",
  ];
  for (let i = 0; i < nfdTxt.length; i++) {
    value = value.replace(nfdTxt[i], nfcTxt[i]);
  }
  return value;
};
